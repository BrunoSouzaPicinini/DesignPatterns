package facade;

public class Tuner {

    public void on() {
        System.out.println("Tuner is on");
    }

    public void off() {
        System.out.println("Tuner is off");
    }

    public void setAm() {
        System.out.println("Tuner is set for AM input");
    }

    public void setFm() {
        System.out.println("Tuner is set for FM input");
    }

    public void setFrequency() {
        System.out.println("Tuner is set to frequency");
    }
}
